# filename: Operators.py
# Topic name: Arithmetic Operators
# Define: Operator are those symbols which is used to perform operation on operands.
# Variables
a = 17
b = 3

# Addition
print("Addition:", a + b)  

# Subtraction
print("Subtraction:", a - b) 

# Multiplication
print("Multiplication:", a * b)  

# Division
print("Division:", a / b) 

# Floor Division
print("Floor Division:", a // b)  

# Modulus
print("Modulus:", a % b) 

# Exponentiation
print("Exponentiation:", a ** b) 
